package com.webapp.formbeans;

public class Stocksearchbean {

	private String stockname;
	
	private String stockcode;
	
	private String instrumentcode;
	
	private String candle;
	
	private String strategy;
	
	private String shortduration;
	
	private String longduration;
	
	public String getStockname() {
		return stockname;
	}

	public void setStockname(String stockname) {
		this.stockname = stockname;
	}

	public String getStockcode() {
		return stockcode;
	}

	public void setStockcode(String stockcode) {
		this.stockcode = stockcode;
	}

	public String getInstrumentcode() {
		return instrumentcode;
	}

	public void setInstrumentcode(String instrumentcode) {
		this.instrumentcode = instrumentcode;
	}

	public String getCandle() {
		return candle;
	}

	public void setCandle(String candle) {
		this.candle = candle;
	}

	public String getStrategy() {
		return strategy;
	}

	public void setStrategy(String strategy) {
		this.strategy = strategy;
	}

	public String getShortduration() {
		return shortduration;
	}

	public void setShortduration(String shortduration) {
		this.shortduration = shortduration;
	}

	public String getLongduration() {
		return longduration;
	}

	public void setLongduration(String longduration) {
		this.longduration = longduration;
	}
	
	
	
	
	
}
